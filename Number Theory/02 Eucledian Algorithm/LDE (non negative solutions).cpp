int extended_euclidean(int a, int b, int& x, int& y) {
    if (b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    int x1, y1;
    int d = extended_euclidean(b, a % b, x1, y1);
    x = y1;
    y = x1 - y1 * (a / b);
    return d;
}

int modInverse(int a, int m) {
    int x, y;
    int g = extended_euclidean(a, m, x, y);
    x = (x % m + m) % m;
    return x;
}

void solve() {
    ll x, y, z, a, b, d, g;
    cin >> a >> b >> d;
    // a*x + b*y = d

    if(d == 0) {
        cout << 1 << endl;
        return;
    }

    g = extended_euclidean(a, b, x, y);

    if(d % g) {
        cout << 0 << endl;
        return;
    }

    int min_y = ((d % a) * modInverse(b, a)) % a;

    // ax+by = d
    // ax = d - by
    // if(d - b.y_min < 0) no solution
    if(d < min_y * b) {
        cout << 0 << endl;
        return;
    }

    // y_min + n*a <= d/b
    // n <= (d/b - y_min) / a

    n = ((d / b) - min_y) / a;
    cout << n + 1 << endl;
}
 
